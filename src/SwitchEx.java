public class SwitchEx {

    public static void main(String[] args) {

        int month=2;

        switch (month){
            case 1:
                System.out.println("jan");
                break;
            case 2:
                System.out.println("feb");
                break;
            default:
                System.out.println("Bleh");
        }
    }
}
